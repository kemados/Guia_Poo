
package EjerciciosGuia;

/*
Define variables donde puedas alojar los resultados y prueba usar dos operadores de cada tipo.
*/

public class Ejercicio3 {
    public static void main(String[] args) {
        
        int num1 = 10;
        int num2 = 4;
        int suma = num1 + num2;
        int resta = num1 - num2;
        double division = num1 / num2;
        boolean bandera = num2 < num1;
        
        System.out.println ("Su suma es de: " + suma); 
        System.out.println ("Su resta es de: " + resta); 
        System.out.println ("Su division es de: " + division);   
        System.out.println ("Su primer numero es mayor que el segundo " + bandera);    
    }
}
